a=int(input('Enter a :'))
b=int(input('Enter b :'))
##input always accepts the input as string
print(a,'+',b,'=',a+b)
print(a,'-',b,'=',a-b)
print(a,'*',b,'=',a*b)
##pure division
print(a,'/',b,'=',a/b)
print(a,'%',b,'=',a%b)
##integer division after . everything is truncated
print(a,'//',b,'=',a//b)
## power in python
print(a,'pow',b,'=',a**b)
## square root in python
print(a,'sqrt',b,'=',a**0.5)
